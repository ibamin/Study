package javabook.chapter3;

public class BitLogicExample {
    public static void main(String[] args) {
        System.out.println("45 & 25 = "+(48&25));
        System.out.println("48 | 25 = "+(48|25));
        System.out.println("48 ^ 25 ="+(48^25));
        System.out.println("~45 = " +(~45));
    }
}
